import java.lang.*;
class Variables
{
	public static void main(String arg[])
	{
		byte b=5;
		System.out.println(b);
		b = 25;
		System.out.println(b);
		short s=300;
		int i=100;
		
		float f=23.5f;
		char c='A';
		System.out.println(s);
		System.out.println(i);
		System.out.println(f);
		System.out.println(c);
		
		
	}
}